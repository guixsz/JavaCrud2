    private void ExcluirFuncionario(){
            String id;
            
            id = txtCodigo.getText();
            
            FuncionarioDTO objfuncionariodto = new FuncionarioDTO();
            objfuncionariodto.setId(id);
            
            FuncionarioDAO objfuncionariodao = new FuncionarioDAO();
            objfuncionariodao.excluirFuncionario(objfuncionariodto);
            
        }
    
    private void LimparCampos(){
            txtCodigo.setText("");
            txtname.setText("");
            intcpf.setText("");
            txtemail.setText("");
            txtpass.setText("");
            txtname.requestFocus();
        }
    
    private void CarregarCampos(){
            int setar = tabelaFuncionario.getSelectedRow();
            
            txtCodigo.setText(tabelaFuncionario.getModel().getValueAt(setar, 0).toString());
            txtname.setText(tabelaFuncionario.getModel().getValueAt(setar, 1).toString());
            intcpf.setText(tabelaFuncionario.getModel().getValueAt(setar,2).toString());
            txtemail.setText(tabelaFuncionario.getModel().getValueAt(setar, 3).toString());
            txtpass.setText(tabelaFuncionario.getModel().getValueAt(setar, 4).toString());
        } 
    
     private void listarValores(){
        
        try {
            FuncionarioDAO objfuncionarioAO = new FuncionarioDAO();
            
            DefaultTableModel model = (DefaultTableModel) tabelaFuncionario.getModel();
            model.setNumRows(0);
            
            ArrayList<FuncionarioDTO> lista = objfuncionarioAO.pesquisarFuncionario();
            
            for(int num = 0; num <lista.size(); num++){
                model.addRow(new Object[]{
                    lista.get(num).getId(),
                    lista.get(num).getNome(),
                    lista.get(num).getCpf(),
                    lista.get(num).getEmail(),
                    lista.get(num).getSenha()
                });
            }
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null, "Lista Valores View" + erro);
        }
    }
